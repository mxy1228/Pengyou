package com.cyou.mrd.pengyou.utils;

import java.util.ArrayList;
import java.util.List;

import com.cyou.mrd.pengyou.entity.ContactFriendItem;

public class HeaderInfo {

	private String name;

	private List<ContactFriendItem> productList = new ArrayList<ContactFriendItem>();

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public ArrayList<ContactFriendItem> getProductList() {
		return (ArrayList<ContactFriendItem>) productList;
	}

	public void setProductList(ArrayList<ContactFriendItem> productList) {
		this.productList = productList;
	}

	public HeaderInfo(String name, List<ContactFriendItem> productList) {
		this.name = name;
		this.productList = productList;
	}

}
