package com.cyou.mrd.pengyou.entity;

public class RelationCircleMessageItem {

	private String type;
	private int mid;//消息id
	private int uid;
	private String nickname;
	private String usrpicture;
	private int aid;//评论的动态id
	private String msg;
	private String acttext;//动态内容段落
	private String actpic;//动态缩略图
	private String msgmemo;//消息简介
	private int hasread;//是否已读 0-未读 1-已读
	private int srctype;//消息来源 "1"-关系圈 "2"-游戏圈
	private String gmname;
	private long timestamp;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public int getMid() {
		return mid;
	}

	public void setMid(int mid) {
		this.mid = mid;
	}

	public int getUid() {
		return uid;
	}

	public void setUid(int uid) {
		this.uid = uid;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getUsrpicture() {
		return usrpicture;
	}

	public void setUsrpicture(String usrpicture) {
		this.usrpicture = usrpicture;
	}

	public int getAid() {
		return aid;
	}

	public void setAid(int aid) {
		this.aid = aid;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public String getActtext() {
		return acttext;
	}

	public void setActtext(String acttext) {
		this.acttext = acttext;
	}

	public String getActpic() {
		return actpic;
	}

	public void setActpic(String actpic) {
		this.actpic = actpic;
	}

	public String getMsgmemo() {
		return msgmemo;
	}

	public void setMsgmemo(String msgmemo) {
		this.msgmemo = msgmemo;
	}

	public int getHasread() {
		return hasread;
	}

	public void setHasread(int hasread) {
		this.hasread = hasread;
	}

	public long getTimestamp() {
		return timestamp;
	}

	public void setTimestamp(long timestamp) {
		this.timestamp = timestamp;
	}

	public int getSrctype() {
		return srctype;
	}

	public void setSrctype(int srctype) {
		this.srctype = srctype;
	}

	public String getGmname() {
		return gmname;
	}

	public void setGmname(String gmname) {
		this.gmname = gmname;
	}
	
}
