package com.cyou.mrd.pengyou.entity.base;

import java.util.List;

import com.cyou.mrd.pengyou.entity.FriendItem;

public class SNSFriendBase {

	private String errorDescription;
	private String successful;
	private String errorNo;
	private List<FriendItem> data;
	public String getErrorDescription() {
		return errorDescription;
	}
	public void setErrorDescription(String errorDescription) {
		this.errorDescription = errorDescription;
	}
	public String getSuccessful() {
		return successful;
	}
	public void setSuccessful(String successful) {
		this.successful = successful;
	}
	public String getErrorNo() {
		return errorNo;
	}
	public void setErrorNo(String errorNo) {
		this.errorNo = errorNo;
	}
	public List<FriendItem> getData() {
		return data;
	}
	public void setData(List<FriendItem> data) {
		this.data = data;
	}
	
}
