package com.cyou.mrd.pengyou.viewcache;

import android.view.View;
import android.widget.ImageView;
import android.widget.TextView;

import com.cyou.mrd.pengyou.R;
import com.cyou.mrd.pengyou.widget.RoundImageView;

public class RelationCircleMessageViewCache {

	private View mView;
	private RoundImageView mAvatarIV;
	private TextView mNameTV;
	private TextView mTypeTV;
	private TextView mContentTV;
	private TextView mTimeTV;
	private ImageView mRightIV;
	private TextView mRightTV;
	private TextView mFromTV;
	
	public RelationCircleMessageViewCache(View view){
		this.mView = view;
	}

	public RoundImageView getmAvatarIV() {
		if(mAvatarIV == null){
			mAvatarIV = (RoundImageView)mView.findViewById(R.id.relation_circle_message_item_avatar);
		}
		return mAvatarIV;
	}

	public TextView getmNameTV() {
		if(mNameTV == null){
			mNameTV = (TextView)mView.findViewById(R.id.relation_circle_message_item_name_tv);
		}
		return mNameTV;
	}

	public TextView getmTypeTV() {
		if(mTypeTV == null){
			mTypeTV = (TextView)mView.findViewById(R.id.relation_circle_message_item_type_tv);
		}
		return mTypeTV;
	}

	public TextView getmContentTV() {
		if(mContentTV == null){
			mContentTV = (TextView)mView.findViewById(R.id.relation_circle_message_item_content_tv);
		}
		return mContentTV;
	}

	public TextView getmTimeTV() {
		if(mTimeTV == null){
			mTimeTV = (TextView)mView.findViewById(R.id.relation_circle_message_item_time_tv);
		}
		return mTimeTV;
	}

	public ImageView getmRightIV() {
		if(mRightIV == null){
			mRightIV = (ImageView)mView.findViewById(R.id.relation_circle_message_item_right_iv);
		}
		return mRightIV;
	}

	public TextView getmRightTV() {
		if(mRightTV == null){
			mRightTV = (TextView)mView.findViewById(R.id.relation_circle_message_item_right_tv);
		}
		return mRightTV;
	}

	public TextView getmFromTV() {
		if(mFromTV == null){
			mFromTV = (TextView)mView.findViewById(R.id.relation_circle_message_item_from_tv);
		}
		return mFromTV;
	}

}
