package com.cyou.mrd.pengyou.viewcache;

import com.cyou.mrd.pengyou.R;

import android.view.View;
import android.widget.ImageView;
import android.widget.TextView;

public class ClassifyItemViewCache {

	private ImageView mIV;
	private TextView mTV;
	private View mView;
	
	public ClassifyItemViewCache(View view){
		this.mView = view;
	}

	public ImageView getmIV() {
		if(mIV == null){
			mIV = (ImageView)mView.findViewById(R.id.classify_item_iv);
		}
		return mIV;
	}

	public TextView getmTV() {
		if(mTV == null){
			mTV = (TextView)mView.findViewById(R.id.classify_item_tv);
		}
		return mTV;
	}
	
	
}
