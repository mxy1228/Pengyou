package com.cyou.mrd.pengyou.viewcache;

import android.view.View;
import android.widget.Button;
import android.widget.ImageView;
import android.widget.LinearLayout;
import android.widget.RatingBar;
import android.widget.TextView;

import com.cyou.mrd.pengyou.R;

public class GameSpecailDetailViewCache {

    private View mView;
    private ImageView imgGameIcon;
    private TextView txtGameName;
    private TextView txtPlayerCount;
    private Button btnDownloadGame;
    private RatingBar ratingBar;
    
    private TextView mSpecailDetailName;
    private TextView mSpecailDetailTime;
    private ImageView mSpecailDetailPicture;
    private TextView mSpecailDetailDesc;
    
    private LinearLayout mSpecailDetailToplayout;
    private LinearLayout mSpecailDetailItemlayout;

    public GameSpecailDetailViewCache(View view) {
        this.mView = view;
    }

    public View getmView() {
        return mView;
    }

    public ImageView getImgGameIcon() {
        if (imgGameIcon == null) {
            imgGameIcon = (ImageView) mView.findViewById(R.id.img_gameicon);
        }
        return imgGameIcon;
    }

    public TextView getTxtGameName() {
        if (txtGameName == null) {
            txtGameName = (TextView) mView.findViewById(R.id.txt_gamename);
        }
        return txtGameName;
    }

    public TextView getTxtPlayerCount() {
        if (txtPlayerCount == null) {
            txtPlayerCount = (TextView) mView
                    .findViewById(R.id.txt_playercount);
        }
        return txtPlayerCount;
    }

    public RatingBar getRatingBar() {
        if (ratingBar == null) {
            ratingBar = (RatingBar) mView.findViewById(R.id.rb_game);
        }
        return ratingBar;
    }

    public Button getBtnDownloadGame() {
        if (btnDownloadGame == null) {
            btnDownloadGame = (Button) mView
                    .findViewById(R.id.btn_playgame);
        }
        return btnDownloadGame;
    }

    public TextView getmSpecailDetailName() {
        if (mSpecailDetailName == null) {
            mSpecailDetailName = (TextView) mView.findViewById(R.id.game_special_detail_name);
        }
        return mSpecailDetailName;
    }

    public TextView getmSpecailDetailTime() {
        if (mSpecailDetailTime == null) {
            mSpecailDetailTime = (TextView) mView.findViewById(R.id.game_special_detail_time);
        }
        return mSpecailDetailTime;
    }

    public ImageView getmSpecailDetailPicture() {
        if (mSpecailDetailPicture == null) {
            mSpecailDetailPicture = (ImageView) mView.findViewById(R.id.game_special_detail_picture);
        }
        return mSpecailDetailPicture;
    }

    public TextView getmSpecailDetailDesc() {
        if (mSpecailDetailDesc == null) {
            mSpecailDetailDesc = (TextView) mView.findViewById(R.id.game_special_detail_desc);
        }
        return mSpecailDetailDesc;
    }

    public LinearLayout getmSpecailDetailToplayout() {
        if (mSpecailDetailToplayout == null) {
            mSpecailDetailToplayout = (LinearLayout) mView.findViewById(R.id.game_specail_detail_top_layout);
        }
        return mSpecailDetailToplayout;
    }

    public LinearLayout getmSpecailDetailItemlayout() {
        if (mSpecailDetailItemlayout == null) {
            mSpecailDetailItemlayout = (LinearLayout) mView.findViewById(R.id.game_specail_detail_item_layout);
        }
        return mSpecailDetailItemlayout;
    }

}
