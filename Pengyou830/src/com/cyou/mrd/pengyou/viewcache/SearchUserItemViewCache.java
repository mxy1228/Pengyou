package com.cyou.mrd.pengyou.viewcache;

import android.view.View;
import android.widget.Button;
import android.widget.TextView;

import com.cyou.mrd.pengyou.R;
import com.cyou.mrd.pengyou.widget.RoundImageView;

public class SearchUserItemViewCache {

	private View mView;
	private TextView mTopTV;
	private RoundImageView mAvatarIV;
	private TextView mNickNameTV;
	private TextView mGameCountTV;
	private TextView mContentTV;
	private Button mFocusIBtn;
	
	public SearchUserItemViewCache(View view){
		this.mView = view;
	}

	public TextView getmTopTV() {
		if(mTopTV == null){
			mTopTV = (TextView)mView.findViewById(R.id.you_may_know_item_top_tv);
		}
		return mTopTV;
	}

	public RoundImageView getmAvatarIV() {
		if(mAvatarIV == null){
			mAvatarIV = (RoundImageView)mView.findViewById(R.id.you_may_known_item_avatar_iv);
		}
		return mAvatarIV;
	}

	public TextView getmNickNameTV() {
		if(mNickNameTV == null){
			mNickNameTV = (TextView)mView.findViewById(R.id.you_may_known_item_nickname_tv);
		}
		return mNickNameTV;
	}

	public TextView getmGameCountTV() {
		if(mGameCountTV == null){
			mGameCountTV = (TextView)mView.findViewById(R.id.you_may_known_item_game_count_tv);
		}
		return mGameCountTV;
	}

	public TextView getmContentTV() {
		if(mContentTV == null){
			mContentTV = (TextView)mView.findViewById(R.id.you_may_known_item_content_tv);
		}
		return mContentTV;
	}

	public Button getmFocusIBtn() {
		if(mFocusIBtn == null){
			mFocusIBtn = (Button)mView.findViewById(R.id.you_may_known_focus_ibtn);
		}
		return mFocusIBtn;
	}
	
	
}
